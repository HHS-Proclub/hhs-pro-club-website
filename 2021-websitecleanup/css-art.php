<h2>Getting Started</h2>
<ol>
    <li>
        Go to <a href="http://codepen.io">CodePen</a> and click <em>New Pen</em> at the top right. CodePen is a nice tool to play around with HTML/CSS/JS and immediately see the results.
    </li>
    <li>
        First try making some basic shapes, like a square & circle (hint: <a href="https://davidwalsh.name/demo/css-rounded-corners.php" target="_blank">border-radius</a>).
    </li>
    <li>
        Use <code>position: absolute</code> and the <code>top</code> & <code>left</code> css properties to position the shapes.
    </li>
    <li>
        Try making one or more of the following, which can be done by arranging various shapes with different colors:
        <ul>
            <li><a href="http://www.vectorportal.com/img_novi/beautiful-iphone.jpg" target="_blank">iPhone</a> (scroll down for an example)</li>
            <li><a href="http://coloringhub.com/wp-content/uploads/2013/11/rainbow.gif" target="_blank">Rainbow</a></li>
            <li><a href="https://openclipart.org/image/800px/svg_to_png/28497/purzen-House-icon.png" target="_blank">House</a></li>
        </ul>
    </li>
    <li>Be proud and email your design to the officers so they can put it up on this page as an example!</li>
</ol>
