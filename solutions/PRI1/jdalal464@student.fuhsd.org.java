
import java.util.Scanner;

public class PRI1 {

	public static void main(String[] args) {
		
		Scanner kboard = new Scanner(System.in);
		int T = kboard.nextInt();
		
		for (int i = 1; i <= T; i++) {
			int N = kboard.nextInt();
			int M = kboard.nextInt();
			System.out.println(N + M);

		}
	}

}
