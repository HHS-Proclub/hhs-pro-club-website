<h2>What Are APIs?</h2>
<p>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;APIs are ways for every programmer, such as yourself, to access already developed software. Want to have a Facebook Like button on your website? Use the Facebook API. Want to have your program get data from Wikipedia? Use the Wikipedia API. Want to programmatically do your math homework? Use the Wolfram Alpha API.
    <br/><br/>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Why do companies offer APIs? Letting other people access their API allows a company to expand its services. For example, as developers create more apps with Facebook integration, more people will use Facebook. In addition, through APIs companies can moderate the requests to their servers and keep information confidential. If all developers request data from servers in the same way (using an API), services can handle an overload of requests in a more elegant manner.
</p>

